class Solution75 {
    public void sortColors(int[] nums) {
        // bubbleSort(nums);
//        mapSolution(nums);
        int length = nums.length;
        int begin = 0;
        for (int i = 0; i < length; i++) {
            if (nums[i] == 0) {
                int temp = nums[i];
                nums[i] = nums[begin];
                nums[begin] = temp;
                begin++;
            }
        }
        for (int i = begin; i < length; i++) {
            if (nums[i] == 1) {
                int temp = nums[i];
                nums[i] = nums[begin];
                nums[begin] = temp;
                begin++;
            }
        }

    }

    public void sortColors2(int[] nums) {
        int n = nums.length;
        int p0 = 0, p1 = 0;
        for (int i = 0; i < n; ++i) {
            if (nums[i] == 1) {
                int temp = nums[i];
                nums[i] = nums[p1];
                nums[p1] = temp;
                ++p1;
            } else if (nums[i] == 0) {
                int temp = nums[i];
                nums[i] = nums[p0];
                nums[p0] = temp;
                if (p0 < p1) {
                    temp = nums[i];
                    nums[i] = nums[p1];
                    nums[p1] = temp;
                }
                ++p0;
                ++p1;
            }
        }
    }

    private void mapSolution(int[] nums) {
        Map<Integer, Integer> countMap = new HashMap<>(3);
        for (int i = 0; i < nums.length; i++) {
            Integer count = countMap.getOrDefault(nums[i], 0);
            countMap.put(nums[i], count + 1);
        }
        List<Integer> colorList = new ArrayList<>();
        colorList.add(0);
        colorList.add(1);
        colorList.add(2);

        int begin = 0;
        for (Integer eachColor : colorList) {
            Integer count1 = countMap.get(eachColor);
            if (count1 != null) {
                for (int i = 0; i < count1; i++) {
                    nums[begin++] = eachColor;
                }
            }
        }
    }

    private void bubbleSort(int[] nums) {
        for (int i = 0; i < nums.length; i++) {
            boolean isSwap = false;
            for (int j = 1; j < nums.length - i; j++) {
                if (nums[j - 1] > nums[j]) {
                    int temp = nums[j - 1];
                    nums[j - 1] = nums[j];
                    nums[j] = temp;
                    isSwap = true;
                }
            }
            if (!isSwap) {
                break;
            }
        }
    }
}
